    public void tampil() {

        try {
            model.setRowCount(0);

            Connection conn = datacaffegui02.Koneksi.koneksiDb();
            String sql = "SELECT*FROM bahan_gudang";
            PreparedStatement pst = conn.prepareStatement(sql);

            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                String id_bahan = rs.getString("id_bahan");
                String namaBahan = rs.getString("nama_bahan");
                int jumlah = rs.getInt("jumlah");
                String satuan = rs.getString("satuan");
                String kadaluarsa = rs.getString("kadaluarsa");

                model.addRow(new Object[]{id_bahan, namaBahan, jumlah, satuan, kadaluarsa});
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error!");
        }
    }

