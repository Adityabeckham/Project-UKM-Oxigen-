    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow >= 0) {
            String namaBahan = (String) jTable1.getValueAt(selectedRow, 1);
            int jumlah = Integer.parseInt(jTable1.getValueAt(selectedRow, 2).toString());
            String satuan = (String) jTable1.getValueAt(selectedRow, 3);
            String kadaluarsa = (String) jTable1.getValueAt(selectedRow, 4);

            DtambahMenu dialog = new DtambahMenu(this, true);
            dialog.setFormData(namaBahan, jumlah, satuan, kadaluarsa, selectedRow);
            dialog.setLocationRelativeTo(null);
            dialog.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(this, "Pilih data yang mau diEdit !");
        }
    }//GEN-LAST:event_btn_editActionPerformed

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:

        try {
            int baris_terpilih = jTable1.getSelectedRow();

            // Validasi
            if (baris_terpilih == -1) {
                JOptionPane.showMessageDialog(this, "Pilih baris dulu masPur!");
                return;
            }

            String ID_bahan = jTable1.getValueAt(baris_terpilih, 0).toString();

            int confirm = JOptionPane.showConfirmDialog(this,
                    "Yakin mau dihapus ?", "KONFIRMASI", JOptionPane.YES_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                String sql = "DELETE FROM bahan_gudang WHERE id_bahan = ?";
                Connection conn = datacaffegui02.Koneksi.koneksiDb();
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, ID_bahan);

                int baris_diganti = pst.executeUpdate();
                if (baris_diganti > 0) {
                    JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus MasPur!");

                    model = (DefaultTableModel) jTable1.getModel();
                    model.removeRow(baris_terpilih);

                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus data MasPur!");
                }

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menghapus data:"+e.getMessage());
            System.err.println("Error: "+e.getMessage());
        }

    }//GEN-LAST:event_btn_hapusActionPerformed

