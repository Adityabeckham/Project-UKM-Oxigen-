    private void btn_accActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_accActionPerformed
        // TODO add your handling code here:
        String namaKasir = tf_namaKasir.getText();
        String shift = cbb_shift.getSelectedItem().toString();
        String role = "barista";

        LocalDate today = LocalDate.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd-MM-yyyy");
        String formattedDate = today.format(formatter);

        if(namaKasir.isBlank()|| namaKasir.equals("Masukkan nama Anda...") || shift.isBlank()){
            JOptionPane.showMessageDialog(this, "Nama Kasir atau shift ga boleh kosong MasPur!"
                ,"INGPO MasPur!", JOptionPane.ERROR_MESSAGE);
        }else{
            try{

                // shift akun
                Connection conn = datacaffegui02.Koneksi.koneksiDb();
                String sql_shift
                = "INSERT INTO shift (id_shift,nama,shift,id_user,tgl)"
                + "VALUES(?,?,?,?,?)";
                PreparedStatement pst_shift = conn.prepareStatement(sql_shift);

                String id_shift = generateID_shift(conn);

                // get ID_user
                String id_user = "";
                String sqlID_user = "SELECT id_user from user WHERE role = ?";
                PreparedStatement pstID_user = conn.prepareStatement(sqlID_user);
                pstID_user.setString(1, role);

                ResultSet rsID_user = pstID_user.executeQuery();

                if(rsID_user.next()){
                    id_user = rsID_user.getString("id_user");
                }

                // INSERT ke table shift
                pst_shift.setString(1, id_shift);
                pst_shift.setString(2, namaKasir);
                pst_shift.setString(3, shift);
                pst_shift.setString(4, id_user);
                pst_shift.setDate(5, java.sql.Date.valueOf(today));

                pst_shift.executeUpdate();
                pst_shift.close();

                // SHIFT-USER
                String sqlShift_User = "INSERT INTO shift_user(id_user,id_shift) VALUES(?,?)";
                PreparedStatement pst_Shift_user = conn.prepareStatement(sqlShift_User);
                pst_Shift_user.setString(1, id_user);
                pst_Shift_user.setString(2, id_shift);

                pst_Shift_user.executeUpdate();
                pst_Shift_user.close();

                rsID_user.close();
                pstID_user.close();

            }catch(Exception e){
                e.printStackTrace();
            }
            MenuKasir mk = new MenuKasir();
            mk.pack();
            mk.setFormBarista(namaKasir, shift,formattedDate);
            mk.setLocationRelativeTo(null);
            mk.setVisible(true);
            this.dispose();

        }
    }//GEN-LAST:event_btn_accActionPerformed


