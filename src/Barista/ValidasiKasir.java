    public String generateID_shift(Connection conn){
        
        String prefix = "SHF";
        String sql = "SELECT * FROM shift ORDER BY id_shift DESC LIMIT 1";
        String id_baru = "";
        
        try{
            conn = datacaffegui02.Koneksi.koneksiDb();
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                
                String id_lama = rs.getString("id_shift");
                String angka = id_lama.substring(3);
                int nomor = Integer.parseInt(angka);
                
                id_baru = String.format("%s%03d",prefix, nomor + 1);
            }else{
                id_baru = prefix + "001";
            }  
        }catch(Exception e){
            e.printStackTrace();
        }
         
        return id_baru;
    }

